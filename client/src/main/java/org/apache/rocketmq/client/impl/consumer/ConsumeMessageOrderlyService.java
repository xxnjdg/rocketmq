    //每次消费任务最大持续时间，默认为
    //60s ，可以通过 － Drocketmq.client.maxTimeConsumeContinuously 改变默认值
    private final static long MAX_TIME_CONSUME_CONTINUOUSLY =
        Long.parseLong(System.getProperty("rocketmq.client.maxTimeConsumeContinuously", "60000"));
    private final DefaultMQPushConsumerImpl defaultMQPushConsumerImpl;
    private final DefaultMQPushConsumer defaultMQPushConsumer;
    //顺序消息消费监昕器
    private final MessageListenerOrderly messageListener;
    //消息消费任务队列
    private final BlockingQueue<Runnable> consumeRequestQueue;
    //消息消费线程池
    private final ThreadPoolExecutor consumeExecutor;
    //消息组名
    private final String consumerGroup;
    //消息消费端消息、消费队列锁容器，内部持有
    //ConcurrentMap<MessageQueue, Object> mqLockTable =new ConcurrentHashMap<MessageQueue,
    //Object＞ （） 。
    private final MessageQueueLock messageQueueLock = new MessageQueueLock();
    //调度任务线程池
    private final ScheduledExecutorService scheduledExecutorService;
    private volatile boolean stopped = false;

    public ConsumeMessageOrderlyService(DefaultMQPushConsumerImpl defaultMQPushConsumerImpl,
        MessageListenerOrderly messageListener) {
        this.defaultMQPushConsumerImpl = defaultMQPushConsumerImpl;
        this.messageListener = messageListener;

        this.defaultMQPushConsumer = this.defaultMQPushConsumerImpl.getDefaultMQPushConsumer();
        this.consumerGroup = this.defaultMQPushConsumer.getConsumerGroup();
        this.consumeRequestQueue = new LinkedBlockingQueue<Runnable>();

        this.consumeExecutor = new ThreadPoolExecutor(
            this.defaultMQPushConsumer.getConsumeThreadMin(),
            this.defaultMQPushConsumer.getConsumeThreadMax(),
            1000 * 60,
            TimeUnit.MILLISECONDS,
            this.consumeRequestQueue,
            new ThreadFactoryImpl("ConsumeMessageThread_"));

        this.scheduledExecutorService = Executors.newSingleThreadScheduledExecutor(new ThreadFactoryImpl("ConsumeMessageScheduledThread_"));
    }

    //如果消费模式为集群模式，启动定时任务，默认每隔 20s 执行一次锁定分配给自 己的
    //消息消费队列 。 通过 －Drocketmq.client.rebalance.locklnterval=20000 设置间隔，该值建议与
    //一次消息负载频率设置相同 。 从上文可知，集群模式下顺序消息消费在创建拉取任务时并
    //未将 ProcessQu巳ue 的 locked 状态设置为 true ， 在未锁定消息队列之前无法执行消息拉取任
    //务， ConsumeM巳ssageOrderlyService 以 每 2 0s 的频率对分配给自己的消息队列进行自动加
    //锁操作，从而消费加锁成功的消息消费队列
    public void start() {
        if (MessageModel.CLUSTERING.equals(ConsumeMessageOrderlyService.this.defaultMQPushConsumerImpl.messageModel())) {
            this.scheduledExecutorService.scheduleAtFixedRate(new Runnable() {
                @Override
                public void run() {
                    ConsumeMessageOrderlyService.this.lockMQPeriodically();
                }
            }, 1000 * 1, ProcessQueue.REBALANCE_LOCK_INTERVAL, TimeUnit.MILLISECONDS);
        }
    }

    public void shutdown(long awaitTerminateMillis) {
        this.stopped = true;
        this.scheduledExecutorService.shutdown();
        ThreadUtils.shutdownGracefully(this.consumeExecutor, awaitTerminateMillis, TimeUnit.MILLISECONDS);
        if (MessageModel.CLUSTERING.equals(this.defaultMQPushConsumerImpl.messageModel())) {
            this.unlockAllMQ();
        }
    }

    public synchronized void unlockAllMQ() {
        this.defaultMQPushConsumerImpl.getRebalanceImpl().unlockAll(false);
    }

